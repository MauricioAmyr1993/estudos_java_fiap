package com.java.igualrelac;

public class OperadorIgualdadeRelacionais {

	public static void main(String[] args) {
		
		int idade = 20;
		boolean maioridade = idade > 18;
		
		System.out.println(maioridade);
		
		// outros operadores de igualdade:
		// x == y - x é igual a y
		// x!= y - x é diferente de y

		// outros operadores relacionais:
		// x > y - x é maior que y
		// x < y - x é menor que y
		// x >= y - x é maior que ou igual a y
		// x <= y - x é menor que ou igual a y
		
		
		

	}

}
